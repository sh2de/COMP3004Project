package team13.cardquest;

public class WeaponCard extends AdventureCard{
    String name = "DUMMYWEAPON";
    int power = 10;

    public WeaponCard(long _id, String _url, String _name, int _power){
        id = _id;
        url = _url;
        name = _name;
        power = _power;
    }

    public int getPower(){
        return power;
    }
}
